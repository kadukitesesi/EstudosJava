public class AreasDiversas {

    static final double pi = 3.14159265358979323846;
    public static double calculaAreaQuadrado(double ladoQuadrado) {
        return  ladoQuadrado * ladoQuadrado;
    }

    public static double calculaAreaCirculo(double raio) {

        return (raio * raio) * pi;
    }

}
